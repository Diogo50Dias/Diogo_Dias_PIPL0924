# Recebe três números
num1 = int(input("Número 1: "))
num2 = int(input("Número 2: "))
num3 = int(input("Número 3: "))

# Calcula maior e menor
maior = max(num1, num2, num3)
menor = min(num1, num2, num3)

# Mostra resultado
print("Maior:", maior)
print("Menor:", menor)